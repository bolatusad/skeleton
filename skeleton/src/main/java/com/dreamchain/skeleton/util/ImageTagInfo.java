package com.dreamchain.skeleton.util;

public class ImageTagInfo {
	private String image_id;
	private String image_url;
	private String name;
	private String type;
	private String round;
	private String accuracy_rate;
	private String count_trained;
	public String getImage_id() {
		return image_id;
	}
	public void setImage_id(String image_id) {
		this.image_id = image_id;
	}
	public String getImage_url() {
		return image_url;
	}
	public void setImage_url(String image_url) {
		this.image_url = image_url;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getRound() {
		return round;
	}
	public void setRound(String round) {
		this.round = round;
	}
	public String getAccuracy_rate() {
		return accuracy_rate;
	}
	public void setAccuracy_rate(String accuracy_rate) {
		this.accuracy_rate = accuracy_rate;
	}
	public String getCount_trained() {
		return count_trained;
	}
	public void setCount_trained(String count_trained) {
		this.count_trained = count_trained;
	}
	
	
}
